public class Utils {

	// Impressão do valor do vetor, por meio de
	// recebimento de parametro de vetor de inteiros
	public void imprimeArray(int[] vetorInteiros) {
		
		for (int i=0; i < vetorInteiros.length; i++) {
			
			System.out.println("Posição - " + i + " Valor - " + vetorInteiros[i]);
			
		}
		
	}
	
}
